class Usuario {
  final String nome;
  final String sobrenome;
  final String nascimento;
  final String celular;
  final String email;
  final String cpf;
  final String username;
  final String senha;

  Usuario({
    required this.nome,
    required this.sobrenome,
    required this.nascimento,
    required this.celular,
    required this.email,
    required this.cpf,
    required this.username,
    required this.senha,
  });
}
